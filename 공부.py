def minus(*num):
    sum = num[0]*2
    for number in num:
        sum -= number
    return sum

print(minus(1,3,4))
# ---------------------------------------------------------------------------------------------------------------------------------------------------------
def mul(*num):
    sum = 1
    for number in num:
        sum *= number
    return sum

print(mul(1,3,4))
